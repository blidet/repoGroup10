package eda397.group10.pojo;

public class UserPOJO {
	
	private String name;
	private String avatar_url;
	
	public UserPOJO(String name, String avatar_url){
		this.name = name;
		this.avatar_url = avatar_url;
	}

	public String getName() {
		return name;
	}

	public String getAvatar_url() {
		return avatar_url;
	}
	
	

}
